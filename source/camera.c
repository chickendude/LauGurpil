void update_camera(Race *race)
{
    Camera *cam = &race->camera;
    Racecar *car = race->car;
    cam->x = (car->x >> 12) - SCREEN_WIDTH / 2;
    cam->y = (car->y >> 12) - SCREEN_HEIGHT / 2;
}

